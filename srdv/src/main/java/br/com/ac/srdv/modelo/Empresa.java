package br.com.ac.srdv.modelo;

public class Empresa {
	private int id;
	private String empresa;

	public int getId() {
		return id;
	}

	public String getEmpresa() {
		return empresa;
	}

	public void setId(int id) {
		this.id = id;
	}

	public void setEmpresa(String empresa) {
		this.empresa = empresa;
	}
}
